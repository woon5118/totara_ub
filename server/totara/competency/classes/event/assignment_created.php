namespace totara_competency\event;

use totara_competency\entity\assignment as assignment_entity;

defined('MOODLE_INTERNAL') || die();

class assignment_created extends assignment {

    /**
     * Initialise required event data properties.
     */
    protected function init() {
        $this->data['crud'] = 'c';
        $this->data['edulevel'] = self::LEVEL_OTHER;
        $this->data['objecttable'] = assignment_entity::TABLE;
    }

    /**
     * Returns localised event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('event_assignment_created', 'totara_competency');
    }

    /**
     * Returns non-localised event description with id's for admin use only.
     *
     * @return string
     */
    public function get_description() {
        return 'new assignment created';
    }
}
